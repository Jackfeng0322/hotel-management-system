    private void jEditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditButtonActionPerformed
            
        if("Choose a Floor".equals(jComboFloor.getSelectedItem())){
            JOptionPane.showMessageDialog(null,"Please Choose A Floor!");
            
            return;
        }
        
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(jEmailField.getText());
    
        try {
            if(matcher.matches()&&!"".equals(jRoomIDField.getText()) && !"".equals(jComboFloor.getSelectedItem()) && !"".equals(jCheckInDate.getDate()) && jCheckInDate.getDate()!=null &&
               !"".equals(jCheckOutDate.getDate())&& jCheckOutDate.getDate()!=null && !"".equals(jCustomerNameField.getText()) &&  !"".equals(jIC_PassportField.getText()) && 
               !"".equals( jContactNoField.getText()) &&  !"".equals(jEmailField.getText())){
            setData();
             JOptionPane.showMessageDialog(this,"Booking Edited Successfully");
              jRoomIDField.setText(null);
              jComboFloor.setSelectedItem("Choose a Floor");
              jCheckInDate.setDate(null);
              jCheckOutDate.setDate(null);
              jCustomerNameField.setText(null);
              jIC_PassportField.setText(null);  
              jContactNoField.setText(null);
              jEmailField.setText(null);
           }
        else{
             JOptionPane.showMessageDialog(this,"Please check the fields and the inputs!");   
                }
        } catch (IOException ex) {
            Logger.getLogger(EditBooking.class.getName()).log(Level.SEVERE, null, ex);
        } // TODO add your handling code here:
    }//GEN-LAST:event_jEditButtonActionPerformed

