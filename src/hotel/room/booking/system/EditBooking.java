       void setData() throws IOException {
            String tempFile = "Temp.txt";
        String currentLine;
        String data[];
        SimpleDateFormat date = new SimpleDateFormat("dd-MM-yyyy");

        int Room = Integer.parseInt(jRoomIDField.getText());
        String Floor = String.valueOf(jComboFloor.getSelectedItem());
        String InDay = date.format(jCheckInDate.getDate());
        String OutDay = date.format(jCheckOutDate.getDate());
        String CN = jCustomerNameField.getText();
        String ICP = jIC_PassportField.getText();
        String CC = jContactNoField.getText();
        String CEM = jEmailField.getText();
        
        try
        {
            FileWriter fw = new FileWriter(tempFile,true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            FileReader fr = new FileReader("addbooking.txt");
            BufferedReader br =new BufferedReader(fr);
            
            while ((currentLine = br.readLine()) != null) {
                data = currentLine.split(",");
       
                if (!data[0].equals("" + Room)) {
                    pw.println(currentLine);
                } else {
                    pw.println (Room +"," + Floor +","+ InDay +","+ OutDay
                        +"," + CN +"," + ICP +","+ CC +"," + CEM +",");
                  
                }
            }
         
            pw.flush();
            fr.close();
            br.close();
            fw.close();
            pw.close();
            bw.close();

        }
        catch (FileNotFoundException ex) {
            EditBooking.errorMesg("Error Occurred", "Title");

        }   catch (IOException ex) {
            java.util.logging.Logger.getLogger(ManageBooking.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        File oldFile = new File ("addbooking.txt");
        File newFile = new File (tempFile);
        System.out.println(oldFile.exists());
        if (oldFile.delete()) {
            System.out.println("Deleted the file: " + oldFile.getName());
        } else {
            System.out.println("Failed to delete the file.");
        }

        newFile.renameTo(new File("addBooking.txt"));

       }
     

